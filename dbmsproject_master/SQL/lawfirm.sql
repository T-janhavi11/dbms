CREATE TABLE `CLIENT`(
CID VARCHAR(5) PRIMARY KEY,
FNAME VARCHAR(50) NOT NULL,
MNAME VARCHAR(50) NOT NULL,
LNAME VARCHAR(50) NOT NULL,
PHONE VARCHAR(10) NOT NULL,
GENDER VARCHAR(1) NOT NULL,
BDAY DATE NOT NULL,
PASSWORD VARCHAR(50) NOT NULL
); ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE ADVOCATE(
FIRSTNAME VARCHAR(50) NOT NULL,
MIDDLENAME VARCHAR(50) NOT NULL,
LASTNAME VARCHAR(50) NOT NULL,
AID VARCHAR(5) PRIMARY KEY,
PHONE VARCHAR(10) NOT NULL,
EMAIL VARCHAR(50) NOT NULL,
CID VARCHAR(5) NOT NULL
); ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE CLERK(
CNAME VARCHAR(50) NOT NULL,
CONTACT VARCHAR(10) NOT NULL,
CLID VARCHAR(5) PRIMARY KEY
); ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE JUDGE(
JID VARCHAR(5) PRIMARY KEY,
JNAME VARCHAR(50) NOT NULL,
EMAIL VARCHAR(50)  NOT NULL,
PHONE VARCHAR(10) NOT NULL,
CASE_ID VARCHAR(5) NOT NULL
); ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE DATA(
DRAFTERID VARCHAR(5) NOT NULL,
CLID VARCHAR(5) NOT NULL
); ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE LaCASE(
CASE_ID VARCHAR(5) PRIMARY KEY,
TYPE VARCHAR(50) NOT NULL,
FEES int(7) NOT NULL,
CASE_DATE DATE NOT NULL,
CASE_TIME TIMESTAMP NOT NULL
); ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE FIRM(
CASE_ID VARCHAR(5) NOT NULL,
AID VARCHAR(5) NOT NULL
); ENGINE=InnoDB DEFAULT CHARSET=latin1;

